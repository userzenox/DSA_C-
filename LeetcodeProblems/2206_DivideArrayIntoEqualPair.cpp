class Solution {
public:
    bool divideArray(vector<int>& nums) {
        int n = nums.size();
        int arr[501] = {0};
       
        for (int i = 0; i < n; i++) {
            arr[nums[i]]++;
        }

        for (int i = 0; i <= 500; i++) { // Fix loop to <= 500
            if (arr[i] % 2 != 0) {
                return false;
            }
        }
        
        return true;
    }
};

